import Foundation

extension Wallpaper {
    var solidColor: UIColor? {
        return Theme.isDarkThemeEnabled ? darkSolidColor : lightSolidColor
    }

    var lightSolidColor: UIColor? {
        switch self {
        case .blush: return UIColor(rgbHex: 0xE26983)
        case .copper: return UIColor(rgbHex: 0xDF9171)
        case .zorba: return UIColor(rgbHex: 0x9E9887)
        case .envy: return UIColor(rgbHex: 0x89AE8F)
        case .sky: return UIColor(rgbHex: 0x32C7E2)
        case .wildBlueYonder: return UIColor(rgbHex: 0x7C99B6)
        case .lavender: return UIColor(rgbHex: 0xC988E7)
        case .shocking: return UIColor(rgbHex: 0xE297C3)
        case .gray: return UIColor(rgbHex: 0xA2A2AA)
        case .eden: return UIColor(rgbHex: 0x146148)
        case .violet: return UIColor(rgbHex: 0x403B91)
        case .eggplant: return UIColor(rgbHex: 0x624249)
        default: return nil
        }
    }

    var darkSolidColor: UIColor? {
        switch self {
        case .blush: return UIColor(rgbHex: 0xA7203D)
        case .copper: return UIColor(rgbHex: 0x8E3310)
        case .zorba: return UIColor(rgbHex: 0x534F41)
        case .envy: return UIColor(rgbHex: 0x36543B)
        case .sky: return UIColor(rgbHex: 0x045867)
        case .wildBlueYonder: return UIColor(rgbHex: 0x2C4D6D)
        case .lavender: return UIColor(rgbHex: 0x6D1D90)
        case .shocking: return UIColor(rgbHex: 0x772254)
        case .gray: return UIColor(rgbHex: 0x4F4F59)
        case .eden: return UIColor(rgbHex: 0x135841)
        case .violet: return UIColor(rgbHex: 0x3B3786)
        case .eggplant: return UIColor(rgbHex: 0x583C41)
        default: return nil
        }
    }
}
